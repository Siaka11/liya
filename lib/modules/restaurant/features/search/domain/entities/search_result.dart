class SearchResult {
  final int id;
  final String name;
  final String imageUrl;
  final double price;
  final String description;
  final String restaurantId;

  SearchResult({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.price,
    required this.description,
    required this.restaurantId,
  });
}
